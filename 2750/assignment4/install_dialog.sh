#this function will decode, unzip and make the file 
#so that all the file that my a4.tar.gz containning will be in the folder 
build(){
  echo "Decoding..."
  match=$(grep --text --line-number '^$installdialog' $0 | cut -d ';' -f 1)
  installdialog_start=$((match + 1))
  tail -n +$installdialog_start $0 | uudecode
  echo "Extracting the file" 
  tar -xzf a4.tar.gz
  echo "Making the File" 
  make > /dev/null
}

#This is the main function where it will crea the folder user need 
#it will also promote user to choose between exporting the 
installfile(){
  echo "please enter a folder name to install" 
  read -e folder_name
  if [ -d $folder_name ] ; then
    echo "the folder already exists"    
    if [ -d $folder_name/lib ] ; then
      echo 'the lib folder already in this ' $folder_name 
      #rm -rf *.o , *.c , *.h , *.y , *.l , *.java , adt , libpm.a , makefile , example1 , *.txt , *.config , MakeTestfile , a4.tar.gz , bin , lib
      exit
    else
      mv -f lib $folder_name
    fi
    if [ -d $folder_name/bin ] ; then
      echo 'the bin folder already in this ' $folder_name 
      #rm -rf *.o , *.c , *.h , *.y , *.l , *.java , adt , libpm.a , makefile , example1 , *.txt , *.config , MakeTestfile , a4.tar.gz , bin , lib
      exit
    else
      mv -f bin $folder_name
    fi
    mv -f README.txt $folder_name
    mv -f pledge.txt $folder_name
    echo "Do you wan to export to bachrc (Y/N)" 
    read -e checktoexport
    if [ $checktoexport = y ] ; then
      path=$(readlink -m $folder_name)
      echo 'export LD_LIBRARY_PATH='$path/lib/':${LD_LIBRARY_PATH}' >> "$HOME"/.bashrc
      echo "export LD_LIBRARY_PATH has been added to ./bashrc"
    else
      echo "you ndde to export LD_LIBRARY_PATH inorder to make IDE work"
    fi
    echo ""
    echo "*******************************************************"
    echo "|you have to exit the terminal to make the bashrc work|"
    echo "*******************************************************"
    echo ""
    echo "cleaning up"
    #rm -rf *.o , *.c , *.h , *.y , *.l , *.java , adt , libpm.a , makefile , example1 , *.txt , *.config , MakeTestfile , a4.tar.gz
  else
    echo "the folder dosnt exist do you want to make the folder (Y/N)"
    read -e check
    if [ $check = y ] ; then
      mkdir $folder_name
      mv -f lib $folder_name
      mv -f bin $folder_name
      mv -f README.txt $folder_name
      mv -f pledge.txt $folder_name
      echo "Do you wan to export to bachrc (Y/N)" 
      read -e checktoexport
      if [ $checktoexport = y ] ; then
	path=$(readlink -m $folder_name)
	echo 'export LD_LIBRARY_PATH='$path/lib/':${LD_LIBRARY_PATH}' >> "$HOME"/.bashrc
        echo "export LD_LIBRARY_PATH has been added to ./bashrc"
      else
	echo "you ndde to export LD_LIBRARY_PATH inorder to make IDE work"
      fi
      echo ""
      echo "*******************************************************"
      echo "|you have to exit the terminal to make the bashrc work|"
      echo "*******************************************************"
      echo ""
      echo "cleaning up"
      #rm -rf *.o , *.c , *.h , *.y , *.l , *.java , adt , libpm.a , makefile , example1 , *.txt , *.config , MakeTestfile , a4.tar.gz
    else 
      echo "installation canceled"  
      #rm -rf *.o , *.c , *.h , *.y , *.l , *.java , adt , libpm.a , makefile , example1 , *.txt , *.config , MakeTestfile , a4.tar.gz , bin , lib
      exit
    fi
  fi
}

if [ "$1" = "--build" ] ; then
    echo "Building..."
    build
elif [ "$1" = "--install" ] ; then
    if [ -f makefile ] ; then
      echo "Installing..."
      installfile
    else
      echo "you have to build first"
      echo "bash instalation_dialog --build  in order to build"
      exit
    fi
else
  echo "doing the full instalation"
  build
  installfile
  echo "finish installing"
fi

exit 

